<script setup>
import { ref } from 'vue';

// –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–∞–¥–∞—é—â–∏—Ö —Ü–≤–µ—Ç–æ–≤
const flowerCount = 100;

// –ú–∞—Å—Å–∏–≤ —Å —ç–º–æ–¥–∑–∏ —Ü–≤–µ—Ç–æ–≤
const flowers = ['üå∏', 'üåº', 'üå∫', 'üíê'];

// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è —Å–ª—É—á–∞–π–Ω–æ–π –ø–æ–∑–∏—Ü–∏–∏ –ø–æ –≥–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª–∏
const getRandomPosition = () => {
  return Math.random() * 100 + 'vw'; // —Å–ª—É—á–∞–π–Ω–∞—è –ø–æ–∑–∏—Ü–∏—è –ø–æ –≥–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª–∏
